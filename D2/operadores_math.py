x = 10
y = 3
z = 7

print(f"{x} + {y} = {x+y}")
print(f"{x} - {y} = {x-y}")
print(f"{x} * {y} = {x*y}")
print(f"{x} / {y} = {x/y}")

print(f"{z} divido al piso de {y} es {z//y}")
print(f"{z} modulado por {y} es {z%y}")

print(f"La raiz cuadrada de {z} es {z**0.5}")
print(f"{x} elevado a la {y} es {x**y}")
print(f"La potencia de {x} a la {y} es {pow(x,y)}")
